// ============================================================================
// Download + optimize the real listing images referenced in seed-listings.mjs.
//
// For each listing slug, fetches the source image(s), resizes to a max
// 1600px wide frame, encodes WebP @ q=78, and writes
// public/properties/<slug>/<n>.webp. Idempotent: existing files are
// skipped unless --force is passed.
//
// Usage:
//   node scripts/download-property-images.mjs [--force]
// ============================================================================

import sharp from "sharp";
import { mkdir, writeFile, stat, readFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { LISTINGS } from "./seed-listings.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT_BASE = path.join(ROOT, "public", "properties");

const FORCE = process.argv.includes("--force");

// Tayara's CDN replies with 403 to fetches that look like obvious bots.
// A real desktop UA is enough — they don't fingerprint further.
const UA =
  "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 " +
  "(KHTML, like Gecko) Chrome/127.0.0.0 Safari/537.36";

async function fileExists(p) {
  try { await stat(p); return true; } catch { return false; }
}

async function downloadOne(url) {
  const res = await fetch(url, {
    headers: {
      "user-agent": UA,
      "accept": "image/avif,image/webp,image/*,*/*;q=0.8",
      "referer": "https://www.tayara.tn/",
    },
  });
  if (!res.ok) throw new Error(`HTTP ${res.status} ${res.statusText}`);
  return Buffer.from(await res.arrayBuffer());
}

async function optimizeAndSave(buf, outPath) {
  await sharp(buf)
    .rotate() // honour EXIF orientation
    .resize({ width: 1600, height: 1600, fit: "inside", withoutEnlargement: true })
    .webp({ quality: 78, effort: 5 })
    .toFile(outPath);
  const { size } = await stat(outPath);
  return size;
}

/**
 * Generate a tiny base64 WebP placeholder (16px wide, low quality) that
 * Next/Image can use as a `placeholder="blur"` data URL. ~600 bytes
 * each; the browser stretches and blurs it during the load.
 */
async function generateBlurDataUrl(buf) {
  const tiny = await sharp(buf)
    .rotate()
    .resize({ width: 16, height: 16, fit: "inside" })
    .webp({ quality: 40, effort: 4 })
    .toBuffer();
  return `data:image/webp;base64,${tiny.toString("base64")}`;
}

let ok = 0;
let skipped = 0;
let failed = 0;
let totalBytes = 0;

// Map of public image path → tiny base64 blur placeholder. Written to
// src/lib/imageBlurMap.ts at the end so PropertyCard can resolve it
// synchronously without an extra fetch.
const blurMap = {};

for (const listing of LISTINGS) {
  const dir = path.join(OUT_BASE, listing.slug);
  await mkdir(dir, { recursive: true });

  for (let i = 0; i < listing.images.length; i++) {
    const src = listing.images[i];
    const outPath = path.join(dir, `${i + 1}.webp`);
    const publicPath = `/properties/${listing.slug}/${i + 1}.webp`;
    const relOut = path.relative(ROOT, outPath).replace(/\\/g, "/");

    const exists = await fileExists(outPath);
    if (!FORCE && exists) {
      // Still build the blur map from disk so we don't lose entries on
      // a re-run that skips downloads.
      try {
        const onDisk = await readFile(outPath);
        blurMap[publicPath] = await generateBlurDataUrl(onDisk);
      } catch { /* ignore — will be rebuilt next --force pass */ }
      skipped++;
      continue;
    }

    try {
      const buf = await downloadOne(src);
      const size = await optimizeAndSave(buf, outPath);
      blurMap[publicPath] = await generateBlurDataUrl(buf);
      totalBytes += size;
      ok++;
      process.stdout.write(`  ✓ ${relOut} (${(size / 1024).toFixed(0)} kB)\n`);
    } catch (e) {
      failed++;
      process.stdout.write(`  ✗ ${relOut} — ${e.message}\n`);
    }
  }
}

// Emit the blur map as a TS module so it can be tree-shaken and bundled
// statically into the client (each entry ≈ 0.5–1 KB; total ≈ 30 KB).
const tsOut = path.join(ROOT, "src", "lib", "imageBlurMap.ts");
const sortedKeys = Object.keys(blurMap).sort();
const tsBody =
  `// AUTO-GENERATED by scripts/download-property-images.mjs — do not edit by hand.\n` +
  `// Tiny 16px WebP blur placeholders for seeded property photos so\n` +
  `// next/image can paint a blurred LQIP while the real image loads.\n\n` +
  `export const IMAGE_BLUR_MAP: Record<string, string> = {\n` +
  sortedKeys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(blurMap[k])},`).join("\n") +
  `\n};\n`;
await writeFile(tsOut, tsBody, "utf8");

console.log("");
console.log(`Done. downloaded=${ok}  skipped=${skipped}  failed=${failed}`);
console.log(`Total bytes written: ${(totalBytes / 1024 / 1024).toFixed(2)} MB`);
console.log(`Wrote blur map: ${path.relative(ROOT, tsOut)} (${sortedKeys.length} entries, ${(tsBody.length / 1024).toFixed(1)} kB)`);
